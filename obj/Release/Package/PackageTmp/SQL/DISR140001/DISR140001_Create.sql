DECLARE @@CNT INT
	, @@CHK VARCHAR(20)
	, @@ERR VARCHAR(MAX);
DECLARE @@MSG_TEXT VARCHAR(MAX);
DECLARE @@MSG_TYPE VARCHAR(MAX);

BEGIN TRY
	SET @@CNT = (SELECT COUNT(1) FROM TB_R_WP_HEADER WHERE PROJECT_CODE = @PROJECT_CODE AND PROJECT_NAME = @PROJECT_NAME);
	IF(@@CNT > 0)
	BEGIN
		SET @@CHK = 'FALSE';
		SET @@ERR = 'Data Has been Registered';
		
	END ELSE
	BEGIN
		INSERT INTO TB_R_WP_HEADER
		(
			 [PROJECT_CODE]
			,[PROJECT_NAME]
			,[ID_TB_M_LOC]
			,[IMPLEMENTATION_DATE]
			,[IMPLEMENTATION_TIME]
			,[DIVISION]
			,[STATUS]
			,[EXECUTOR]
			,[CONTRACTOR]
			,[LEADER]
			,[SUPERVISOR]
			,[CREATED_BY]
			,[CREATED_DT]
			,[CHANGED_BY]
			,[CHANGED_DT]
			
		)
		VALUES
		(
			@PROJECT_CODE,
			@PROJECT_NAME,
			@LOCATION,
			@DATE,
			@TIME,
			@DIVISION,
			@STATUS,
			@EXECUTOR,
			@CONTRACTOR,
			@LEADER_NAME,
			@SUPERVISOR_NAME,
			@USERNAME,
			GETDATE(),
			@USERNAME,
			GETDATE()
		);


		SET @@CHK = 'TRUE';
		SET @@ERR = 'NOTHING';
	END
END TRY
BEGIN CATCH
	SET @@CHK = 'FALSE';
	SET @@ERR = 'ERROR INSERT WP_HEADER:' +@PROJECT_CODE+
	'<br/>Detail Error :|: ' + ERROR_MESSAGE() + ' :|: ';	
END CATCH

SELECT @@CHK AS STACK, @@ERR AS LINE_STS