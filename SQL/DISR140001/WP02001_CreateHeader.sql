DECLARE @@CNT INT
	, @@CHK VARCHAR(20)
	, @@ERR VARCHAR(MAX);
DECLARE @@MSG_TEXT VARCHAR(MAX);
DECLARE @@MSG_TYPE VARCHAR(MAX);

BEGIN TRY
	SET @@CNT = (SELECT COUNT(1) FROM TB_R_WP_HEADER WHERE WP_IMPB_NO = @WP_IMPB_NO);
	IF(@@CNT > 0)
	BEGIN

		UPDATE TB_R_WP_HEADER 
			SET [ID_TB_M_AREA] = @ID_TB_M_AREA,
				[WP_PROJECT_NAME] = @WP_PROJECT_NAME,
				[CHANGED_BY] = @username,
				[CHANGED_DT] = GETDATE()
		WHERE [WP_IMPB_NO] = @WP_IMPB_NO;

		UPDATE TB_R_WP_DETAIL_IDENTITY 
			SET ID_TB_M_LOCATION = @ID_TB_M_LOCATION,
				DEP_OR_DIV_CODE = @DEP_OR_DIV_CODE,
				IMPLEMENT_DATE_FROM = @IMPLEMENT_DATE_FROM,
				IMPLEMENT_DATE_TO = @IMPLEMENT_DATE_TO,
				IMPLEMENT_TIME_FROM = @IMPLEMENT_TIME_FROM,
				WORKING_STATUS = @WORKING_STATUS,
				WORKING_NOTES = @WORKING_NOTES,
				[CHANGED_BY] = @username,
				[CHANGED_DT] = GETDATE()
		WHERE [WP_IMPB_NO] = @WP_IMPB_NO;


		UPDATE TB_R_WP_DETAIL_IMPLEMENTOR 
			SET EXECUTOR = @EXECUTOR,
				ID_TB_M_COMPANY = @ID_TB_M_COMPANY,
				EMPLOYEE_LEAD_PROJECT = @EMPLOYEE_LEAD_PROJECT,
				EMPLOYEE_SUPERVISOR_PROJECT = @EMPLOYEE_SUPERVISOR_PROJECT,
				[CHANGED_BY] = @username,
				[CHANGED_DT] = GETDATE()
		WHERE [WP_IMPB_NO] = @WP_IMPB_NO;

		SET @@CHK = 'TRUE';
		SET @@ERR = 'NOTHING';

	END ELSE
	BEGIN
		INSERT INTO TB_R_WP_HEADER
		(
			 [WP_IMPB_NO]
			,[ID_TB_M_AREA]
			,[WP_PROJECT_NAME]
			,[CREATED_BY]
			,[CREATED_DT]
			,[CHANGED_BY]
			,[CHANGED_DT]
			
		)
		VALUES
		(
			@WP_IMPB_NO,
			@ID_TB_M_AREA,
			@WP_PROJECT_NAME,
			@username,
			GETDATE(),
			@username,
			GETDATE()
		);

		INSERT INTO TB_R_WP_DETAIL_IDENTITY
		(
			 WP_IMPB_NO
			,ID_TB_M_LOCATION
			,DEP_OR_DIV_CODE
			,IMPLEMENT_DATE_FROM
			,IMPLEMENT_DATE_TO
			,IMPLEMENT_TIME_FROM
			,IMPLEMENT_TIME_TO
			,WORKING_STATUS
			,WORKING_NOTES
			,[CREATED_BY]
			,[CREATED_DT]
			,[CHANGED_BY]
			,[CHANGED_DT]
			
		)
		VALUES
		(
			@WP_IMPB_NO,
			@ID_TB_M_LOCATION,
			@DEP_OR_DIV_CODE,
			@IMPLEMENT_DATE_FROM,
			@IMPLEMENT_DATE_TO,
			@IMPLEMENT_TIME_FROM,
			NULL,
			@WORKING_STATUS,
			@WORKING_NOTES,
			@username,
			GETDATE(),
			@username,
			GETDATE()
		);

		INSERT INTO TB_R_WP_DETAIL_IMPLEMENTOR
		(
			 WP_IMPB_NO
			,EXECUTOR
			,ID_TB_M_COMPANY
			,EMPLOYEE_LEAD_PROJECT
			,EMPLOYEE_SUPERVISOR_PROJECT
			,[CREATED_BY]
			,[CREATED_DT]
			,[CHANGED_BY]
			,[CHANGED_DT]
			
		)
		VALUES
		(
			@WP_IMPB_NO,
			@EXECUTOR,
			@ID_TB_M_COMPANY,
			@EMPLOYEE_LEAD_PROJECT,
			@EMPLOYEE_SUPERVISOR_PROJECT,
			@username,
			GETDATE(),
			@username,
			GETDATE()
		);

		SET @@CHK = 'TRUE';
		SET @@ERR = 'NOTHING';
	END
END TRY
BEGIN CATCH
	SET @@CHK = 'FALSE';
	SET @@ERR = 'ERROR INSERT WP_HEADER:' +@WP_IMPB_NO+
	'<br/>Detail Error :|: ' + ERROR_MESSAGE() + ' :|: ';	
END CATCH

SELECT @@CHK AS STACK, @@ERR AS LINE_STS