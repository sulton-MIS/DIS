DECLARE @@CNT INT
	, @@CHK VARCHAR(20)
	, @@ERR VARCHAR(MAX);

DECLARE @@COUNTER INT= (SELECT (CASE WHEN (SELECT COUNT(PROCESS_ID) FROM TB_R_LOG_H) = 0 THEN 1 ELSE (SELECT TOP 1 PROCESS_ID FROM TB_R_LOG_H ORDER BY PROCESS_ID DESC) + 1 END));
EXEC SP_INSERT_UPDATE_LOG_H @USERNAME,'','WP01001',1,'1','Update',@@COUNTER;

DECLARE @@MSG_START VARCHAR(MAX) = 'LOG001 : '+ (SELECT dbo.GenerateMessage('LOG001','','',''));
EXEC SP_INSERT_LOG_D @@COUNTER, 'Start', 'LOG001', 'Info',  @@MSG_START;

BEGIN TRY
	
	UPDATE TB_M_SYSTEM SET 
		SYSTEM_ID= @SYSTEM_ID, 
		SYSTEM_TYPE = @SYSTEM_TYPE,
		SYSTEM_CD	= @SYSTEM_CD,
		SYSTEM_VALID_FR = @SYSTEM_VALID_FR,
		SYSTEM_VALID_TO = @SYSTEM_VALID_TO,
		SYSTEM_VALUE_TXT = @SYSTEM_VALUE_TXT, 
		SYSTEM_VALUE_NUM = @SYSTEM_VALUE_NUM,
		SYSTEM_VALUE_DT = CONVERT(DATE, @SYSTEM_VALUE_DT,102), 
		CHANGED_BY = @USERNAME, 
		CHANGED_DT = GETDATE() 
	WHERE SYSTEM_ID = @ID AND SYSTEM_TYPE = @TYPE
	
	SET @@CHK = 'TRUE';
	SET @@ERR = 'Success';

	DECLARE @@MSG_SUCCESS VARCHAR(MAX) = 'MWP003 : '+ (SELECT dbo.GenerateMessage('MWP003','SYSTEM MASTER','',''));
	EXEC SP_INSERT_LOG_D @@COUNTER, 'Updated', 'MWP003', 'Info',  @@MSG_SUCCESS;

END TRY
BEGIN CATCH
	DECLARE @@MSG_ERROR VARCHAR(MAX) = ERROR_MESSAGE();
	EXEC SP_INSERT_LOG_D @@COUNTER, 'ERROR', 'Error', 'ERROR',  @@MSG_ERROR;

	SET @@CHK = 'FALSE';
	SET @@ERR = 'ERROR UPDATE TB_M_SYSTEM:' +@SYSTEM_ID+
	'<br/>Detail Error :|: ' + ERROR_MESSAGE() + ' :|: ';	
END CATCH

SELECT @@CHK AS STACK, @@ERR AS LINE_STS


DECLARE @@MSG_END VARCHAR(MAX) = 'LOG007 : '+ (SELECT dbo.GenerateMessage('LOG007','','',''));
EXEC SP_INSERT_LOG_D @@COUNTER, 'END', 'LOG007', 'Info',  @@MSG_END;

EXEC SP_INSERT_UPDATE_LOG_H 
@USERNAME,'','WP01001','1',1,'CREATE',@@COUNTER;