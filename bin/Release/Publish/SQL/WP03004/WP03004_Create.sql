DECLARE @@CNT INT
	, @@CHK VARCHAR(20)
	, @@ERR VARCHAR(MAX);
DECLARE @@MSG_TEXT VARCHAR(MAX);
DECLARE @@MSG_TYPE VARCHAR(MAX);

BEGIN TRY
	SET @@CNT = (SELECT COUNT(1) FROM TB_R_LEARN_EXAM_QUESTION WHERE ID_TB_R_LEARN_EXAM_SUBJECT = @ID_TB_R_LEARN_EXAM_SUBJECT AND ID_TB_M_LEARN_QUESTION = @ID_TB_M_LEARN_QUESTION);
	IF(@@CNT > 0)
	BEGIN
		SELECT 
			@@MSG_TEXT = MSG_TEXT,
			@@MSG_TYPE = MSG_TYPE
		FROM dbo.FN_GENERATE_MSG('MPISSTD049E', 'LEARN EXAM QUESTION : ' +@ID_TB_R_LEARN_EXAM_SUBJECT , NULL, NULL, NULL);
		SET @@CHK = 'FALSE';
		SET @@ERR = @@MSG_TEXT;
		
	END ELSE
	BEGIN
		
		INSERT INTO TB_R_LEARN_EXAM_QUESTION
		(
			   ID_TB_R_LEARN_EXAM_SUBJECT
			  ,ID_TB_M_LEARN_QUESTION
			  ,CREATED_BY
			  ,CREATED_DT
			  ,UPDATED_BY
			  ,UPDATED_DT

		)
		VALUES
		(
			@ID_TB_R_LEARN_EXAM_SUBJECT
			,@ID_TB_M_LEARN_QUESTION
			,@USERNAME
			,GETDATE()
			,@USERNAME
			,GETDATE()
		);


		SET @@CHK = 'TRUE';
		SET @@ERR = 'NOTHING';
	END
END TRY
BEGIN CATCH
	SET @@CHK = 'FALSE';
	SET @@ERR = 'ERROR LEARN EXAM QUESTION :' +@ID_TB_R_LEARN_EXAM_SUBJECT+
	'<br/>Detail Error :|: ' + ERROR_MESSAGE() + ' :|: ';	
END CATCH

SELECT @@CHK AS STACK, @@ERR AS LINE_STS
