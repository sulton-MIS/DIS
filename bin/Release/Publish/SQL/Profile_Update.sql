UPDATE TB_M_EMPLOYEE
SET
USERNAME = @USERNAME,
[PASSWORD] = (CASE WHEN @PASSWORD != '' THEN @PASSWORD ELSE [PASSWORD] END),
EMAIL =@EMAIL,
FIRST_NAME = @FIRST_NAME,
LAST_NAME = @LAST_NAME,
PHONE = @PHONE,
GENDER = @GENDER,
BIRTH_DATE = CONVERT(VARCHAR(10),@BIRTH_DATE)
WHERE USERNAME = @USERNAME_OLD;

UPDATE TB_M_USER_MAPPING
SET
ID_TB_M_AREA = @AREA,
ID_TB_M_LOCATION = @LOCATION,
USERNAME = @USERNAME,
EMAIL = @EMAIL,
FIRST_NAME = @FIRST_NAME,
LAST_NAME = @LAST_NAME
WHERE
USERNAME = @USERNAME_OLD