DECLARE @@QUERY VARCHAR(MAX);
SET @@QUERY = '';
SET @@QUERY = '
	UPDATE TB_R_LEARN_EXAM_QUESTION 
		SET IS_DELETED = ' + @ISDELETED + '
	WHERE ID_TB_R_LEARN_EXAM_SUBJECT = ' + @IDSUBJECT +
		' AND ID_TB_M_LEARN_QUESTION IN (' + @IDQUESTIONS+') ';
EXEC(@@QUERY);

UPDATE TB_R_LEARN_EXAM_SUBJECT
	SET IS_PUBLISHED = (SELECT COUNT(*) FROM TB_R_LEARN_EXAM_QUESTION WHERE ID_TB_R_LEARN_EXAM_SUBJECT = @IDSUBJECT AND IS_DELETED = 0)
	WHERE ID_TB_R_LEARN_EXAM_SUBJECT = @IDSUBJECT
